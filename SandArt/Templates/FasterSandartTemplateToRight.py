def on_on_chat():
    startpos = player.position()
    halfsize = picture.length//2
    startpos = positions.add(startpos, pos(0, 0, halfsize))
    blocks.place(GLASS, positions.add(startpos, pos(0, 0, 0)))
    for j in range(halfsize+1):
        for i in range(picture[j].length):
            blocks.place(setblocks[picture[halfsize+j][picture[halfsize+j].length-1-i]], positions.add(startpos, pos(i, i+j+1, j)))
            blocks.place(TORCH, positions.add(startpos, pos(i+1, i+j+1, j)))
            if j > 0:
                blocks.place(setblocks[picture[halfsize-j][picture[halfsize+j].length-1-i]], positions.add(startpos, pos(i, i+j+1, -j)))
                blocks.place(TORCH, positions.add(startpos, pos(i+1, i+j+1, -j)))
        blocks.place(TORCH, positions.add(startpos, pos(0, j+1, -j-1)))
        blocks.place(TORCH, positions.add(startpos, pos(0, j+1, j+1)))
player.on_chat("art", on_on_chat)



